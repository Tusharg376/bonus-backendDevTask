const mongoose = require('mongoose')

const userSchema = new mongoose.Schema({
    name:{
        type:String,
        required:true
    },
    email:{
        type:String,
        required:true,
        unique:true
    },
    password:{
        type:String,
        required:true
    },
    role:{
        type:String,
        enum:["Employee","Admin","Super Admin"],
        default:"Employee"
    }
},{timestamps:true})

module.exports = mongoose.model('user', userSchema)